let palavra01 = "Vou casar!!"
let palavra02 = "05.07.2025"
let palavra03 = "Gabriel ♥ Paula"

// concatenar -> a concatenação é utilizada para unir as variáveis 
// utilizando , apresenta o resultado separando cada variável, dando ali um espaço entre as variáveis 
// utilizando + apresenta o resultado de forma que as variáveis fiquem coladas
// utilizando ${variável} neste caso a concatenação fica da maneira considerada mais adequada, de melhor visualização, melhor para dar manutenção.

console.log(palavra01, palavra02, palavra03)
console.log(palavra01 + palavra02 + palavra03)
console.log(`${palavra01} ${palavra02} ${palavra03}\n ♥♥♥♥♥♥♥♥♥♥♥♥`)